#!/bin/bash

# Создаем файлы с вопросами по Kafka
files=(
    "1.Что такое offset Где хранится.md"
    "2.Какие политики очистки данных поддерживаются Какая взаимосвязь с размером сегмента.md"
    "3.Что есть transaction log.md"
    "4.Партиция. Что это такое На что влияет Кто есть лидер партиции.md"
    "5.Из каких компонентов состоит кластер.md"
    "6.Концепция transaction log.md"
    "7.Гарантии доставки сообщений.md"
    "8.Отказоустойчивость.md"
    "9.Какие гарантии доставки предоставляет Kafka Каким образом это достигается на каждом уровне.md"
    "10.Rebalance. Что это такое В каких случаях возникает.md"
    "11.Какие гарантии порядка предоставляет Kafka.md"
    "12.Какую функцию выполняет Zookeeper в кластере.md"
    "13.Каким образом Kafka работает со сжатыми данными.md"
    "14.Как устроен внутренний механизм работы транзакции.md"
    "15.Что есть zero copy.md"
)

# Создаем каждый файл
for file in "${files[@]}"; do
    # Заменяем проблемные символы в именах файлов
    safe_filename=$(echo "$file" | sed 's/[<>:"/\\|?*]/_/g')
    touch "$safe_filename"
    echo "Создан файл: $safe_filename"
done

echo "Все файлы успешно созданы!"